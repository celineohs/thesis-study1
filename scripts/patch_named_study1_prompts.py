def patch_system_prompt_tail(path: Path, coop: bool, display_name: str, region: str, rag: str):
    t = path.read_text(encoding="utf-8")
    a = "**대화 방식**\n"
    i = t.find(a)
    if i == -1:
        print("no dialogue block", path.name)
        return
    j = t.find('\n"""\n\n# ──────────────────────────────────────────────\n# Session state', i)
    if j == -1:
        j = t.find('\n"""\n\n# ──────────────────────────────────────────────\n# Session', i)
    if j == -1:
        print("no end", path.name)
        return
    new_tail = coop_dialogue_block(display_name, region, rag) if coop else comp_dialogue_block(
        display_name, region, rag
    )
    t = t[:i] + new_tail + t[j:]
    path.write_text(t, encoding="utf-8")


